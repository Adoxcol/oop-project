package javafxapplication18;

/**
 *
 * @author hp
 */
public class Budget {
    private String dep;
    private String exp;

    public Budget(String dep, String exp) {
        this.dep = dep;
        this.exp = exp;
    }

    public String getDep() {
        return dep;
    }

    public void setDep(String dep) {
        this.dep = dep;
    }

    public String getExp() {
        return exp;
    }

    public void setExp(String exp) {
        this.exp = exp;
    }
    
    
}
